import UIKit
extension UIColor {

    public class var commonTint: UIColor {
       return UIColor(named: "commonTint")!
    }
    
    public class var argoTint: UIColor {
       return UIColor(named: "agroTint")!
    }
    
    public class var epicTint: UIColor {
       return UIColor(named: "epicTint")!
    }
    
    public class var legendaryTint: UIColor {
       return UIColor(named: "legendaryTint")!
    }
    
    public class var rareTint: UIColor {
       return UIColor(named: "rareTint")!
    }
    
    public class var notBlack: UIColor {
       return UIColor(named: "notBlack")!
    }

    public class var blueTint: UIColor {
       return UIColor(named: "blueTint")!
    }

    
}


